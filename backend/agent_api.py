# ---------------------------------------------------------------------------
# Auth
# ---------------------------------------------------------------------------

def require_api_key(f):
    """Decorator that validates the Bearer token against stored agent_api_key."""
    @functools.wraps(f)
    def wrapper(*args, **kwargs):
        api_key = get_setting('agent_api_key')
        if not api_key:
            return _err('Agent API key not configured. Run: ./agent-setup.sh --api-key', 'NO_API_KEY', 503)

        auth = request.headers.get('Authorization', '')
        token = None
        if auth.startswith('Bearer '):
            token = auth[7:]
        elif request.args.get('api_key'):
            token = request.args.get('api_key')

        if not token or token != api_key:
            return _err('Invalid or missing API key', 'UNAUTHORIZED', 401)

        return f(*args, **kwargs)
    return wrapper


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _ok(data=None):
    return jsonify({'ok': True, 'data': data or {}}), 200


def _err(message, code='ERROR', status=400):
    return jsonify({'ok': False, 'error': message, 'code': code}), status


def _parse_device(d):
    """Normalize a device dict for API output."""
    if isinstance(d.get('tags'), str):
        try:
            d['tags'] = json.loads(d['tags'])
        except (json.JSONDecodeError, TypeError):
            d['tags'] = []
    elif not d.get('tags'):
        d['tags'] = []
    return d


