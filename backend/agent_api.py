"""Agent API — RESTful endpoints for programmatic access by ClaudeBot, OpenClaw, etc.

All endpoints are under /api/v1/agent/ and require Bearer token auth via the
agent_api_key setting (generated by agent-setup.sh --api-key).

Response envelope:
    { "ok": true, "data": { ... } }
    { "ok": false, "error": "message", "code": "ERROR_CODE" }
"""

