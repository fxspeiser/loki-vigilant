@agent_bp.route('/status', methods=['GET'])
@require_api_key
def agent_status():
    """System status, network info, and service health.

    GET /api/v1/agent/status
    Response: {ok, data: {network, device_count, intrusion_stats, auto_scan_policy, uptime}}
    """
    iface, subnet, gateway = get_default_interface()
    devices = get_all_devices()
    i_stats = get_intrusion_stats()
    policy = get_setting('auto_scan_policy', 'scanners')

    return _ok({
        'network': {
            'interface': iface,
            'subnet': subnet,
            'gateway': gateway,
        },
        'device_count': len(devices),
        'intrusion_stats': {
            'total_attempts': i_stats.get('total_attempts', 0),
            'last_attempt': dict(i_stats['last_attempt']) if i_stats.get('last_attempt') else None,
        },
        'auto_scan_policy': policy,
        'timestamp': datetime.now().isoformat(),
    })


