@agent_bp.route('/bulk/tag', methods=['POST'])
@require_api_key
def agent_bulk_tag():
    """Add a tag to multiple devices at once.

    POST /api/v1/agent/bulk/tag
    Body: {"macs": ["aa:bb:...", "cc:dd:..."], "tag": "critical"}
    Response: {ok, data: {updated: N}}
    """
    data = request.get_json(silent=True) or {}
    macs = data.get('macs', [])
    tag = data.get('tag', '')

    if not macs or not tag:
        return _err('macs (list) and tag (string) are required', 'INVALID_INPUT')

    updated = 0
    for mac in macs:
        device = get_device(mac)
        if not device:
            continue
        tags = get_device_tags(mac)
        if tag not in tags:
            tags.append(tag)
            set_device_tags(mac, tags)
        updated += 1

    return _ok({'updated': updated, 'tag': tag})


@agent_bp.route('/bulk/scan', methods=['POST'])
@require_api_key
def agent_bulk_scan():
    """Trigger port scans on multiple devices. Returns a summary.

    POST /api/v1/agent/bulk/scan
    Body: {"macs": ["aa:bb:...", "cc:dd:..."]}

    Note: Scans run synchronously in sequence. This endpoint may take many minutes.
    For async scanning, use /scan/port for individual devices.
    Response: {ok, data: {results: [{mac, ip, ports_found, vulns_found}, ...], count: N}}
    """
    from backend.scanner import stealth_port_scan

    data = request.get_json(silent=True) or {}
    macs = data.get('macs', [])

    if not macs:
        return _err('macs list is required', 'INVALID_INPUT')

    results = []
    for mac in macs:
        device = get_device(mac)
        if not device:
            results.append({'mac': mac, 'error': 'not found'})
            continue

        ip = device.get('ip')
        if not ip:
            results.append({'mac': mac, 'error': 'no IP'})
            continue

        scan = stealth_port_scan(ip, mac)
        ports_found = len(scan.get('ports', []))
        vulns_found = sum(len(p.get('vulns', [])) for p in scan.get('ports', []))

        results.append({
            'mac': mac,
            'ip': ip,
            'ports_found': ports_found,
            'vulns_found': vulns_found,
            'os': scan.get('os', ''),
            'scan_id': scan.get('scan_id'),
        })

    return _ok({'results': results, 'count': len(results)})
