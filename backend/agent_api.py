@agent_bp.route('/devices', methods=['GET'])
@require_api_key
def agent_devices():
    """List all discovered devices.

    GET /api/v1/agent/devices
    Query params:
        type=<device_type>     Filter by type (router, computer, phone, etc.)
        tag=<tag>              Filter by tag
        new=1                  Only devices seen in the last hour
    Response: {ok, data: {devices: [...], count: N}}
    """
    devices = get_all_devices()
    devices = [_parse_device(d) for d in devices]

    # Filters
    dtype = request.args.get('type')
    if dtype:
        devices = [d for d in devices if d.get('device_type') == dtype]

    tag = request.args.get('tag')
    if tag:
        devices = [d for d in devices if tag in (d.get('tags') or [])]

    if request.args.get('new') == '1':
        now = datetime.now()
        filtered = []
        for d in devices:
            if d.get('first_seen'):
                try:
                    first = datetime.fromisoformat(d['first_seen'])
                    if (now - first).total_seconds() <= 3600:
                        filtered.append(d)
                except (ValueError, TypeError):
                    pass
        devices = filtered

    return _ok({'devices': devices, 'count': len(devices)})


@agent_bp.route('/devices/<mac>', methods=['GET'])
@require_api_key
def agent_device_detail(mac):
    """Get a single device by MAC address.

    GET /api/v1/agent/devices/<mac>
    Response: {ok, data: {device: {...}, scan_history: [...], tags: [...]}}
    """
    device = get_device(mac)
    if not device:
        return _err(f'Device not found: {mac}', 'NOT_FOUND', 404)

    device = _parse_device(device)
    scans = get_scan_history(mac)
    for s in scans:
        s['results'] = json.loads(s['results']) if isinstance(s['results'], str) else s['results']
        s['vulns'] = get_vulns_for_scan(s['id'])

    return _ok({
        'device': device,
        'scan_history': scans,
        'tags': device.get('tags', []),
    })


