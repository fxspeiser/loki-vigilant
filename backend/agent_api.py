@agent_bp.route('/scan/discover', methods=['POST'])
@require_api_key
def agent_discover():
    """Trigger a network discovery scan (ARP + nmap ping).

    POST /api/v1/agent/scan/discover
    Response: {ok, data: {count: N, devices: [...]}}

    Note: This is synchronous and may take 10-30 seconds.
    """
    from backend.scanner import arp_scan, nmap_discovery

    devices = arp_scan()
    try:
        nmap_devices = nmap_discovery()
        dev_by_mac = {d['mac']: d for d in devices}
        for nd in nmap_devices:
            if nd['mac'] in dev_by_mac:
                existing = dev_by_mac[nd['mac']]
                if nd.get('vendor') and not existing.get('vendor'):
                    existing['vendor'] = nd['vendor']
                if nd.get('hostname') and not existing.get('hostname'):
                    existing['hostname'] = nd['hostname']
            else:
                devices.append(nd)
    except Exception:
        pass

    return _ok({'count': len(devices), 'devices': devices})


@agent_bp.route('/scan/port', methods=['POST'])
@require_api_key
def agent_port_scan():
    """Trigger a port scan on a device. Runs synchronously and returns results.

    POST /api/v1/agent/scan/port
    Body: {"ip": "192.168.1.100", "mac": "aa:bb:cc:dd:ee:ff"}
    Response: {ok, data: {ports: [...], os: "...", vulnerabilities: [...]}}

    Note: This is synchronous and may take 1-5 minutes.
    """
    from backend.scanner import stealth_port_scan

    data = request.get_json(silent=True) or {}
    ip = data.get('ip')
    mac = data.get('mac')
    if not ip or not mac:
        return _err('ip and mac are required', 'INVALID_INPUT')

    results = stealth_port_scan(ip, mac)
    if 'error' in results:
        return _err(results['error'], 'SCAN_FAILED')

    return _ok(results)


