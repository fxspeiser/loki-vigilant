@agent_bp.route('/vulnerabilities', methods=['GET'])
@require_api_key
def agent_vulnerabilities():
    """Get all vulnerabilities across all devices, sorted by severity.

    GET /api/v1/agent/vulnerabilities?severity=CRITICAL&limit=100
    Response: {ok, data: {vulnerabilities: [...], count: N}}
    """
    severity_filter = request.args.get('severity', '').upper()
    limit = request.args.get('limit', 100, type=int)

    devices = get_all_devices()
    all_vulns = []

    for device in devices:
        mac = device['mac']
        scans = get_scan_history(mac)
        if not scans:
            continue
        # Only latest scan per device
        latest = scans[0]
        results = json.loads(latest['results']) if isinstance(latest['results'], str) else latest['results']
        vulns = get_vulns_for_scan(latest['id'])

        for v in vulns:
            v['device_mac'] = mac
            v['device_ip'] = device.get('ip', '')
            v['device_name'] = device.get('nickname') or device.get('hostname') or device.get('ip', '')
            v['scan_time'] = latest.get('scan_time', '')
            all_vulns.append(v)

    # Filter by severity
    if severity_filter:
        all_vulns = [v for v in all_vulns if v.get('severity', '').upper() == severity_filter]

    # Sort by severity priority
    sev_order = {'CRITICAL': 0, 'HIGH': 1, 'MEDIUM': 2, 'LOW': 3}
    all_vulns.sort(key=lambda v: sev_order.get(v.get('severity', '').upper(), 99))

    all_vulns = all_vulns[:limit]

    return _ok({'vulnerabilities': all_vulns, 'count': len(all_vulns)})


